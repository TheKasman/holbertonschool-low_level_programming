#include "3-calc.h"

/**
 * op_add - Addition
 * @a: number 1
 * @b: number 2
 * Return: Returns the calculation as an int
 */
int op_add(int a, int b)
{
	return (a + b);
}


/**
 * op_sub - Subtraction
 * @a: number 1
 * @b: number 2
 * Return: Returns the calculation as an int
 */

int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - Multiplication
 * @a: number 1
 * @b: number 2
 * Return: Returns the calculation as an int
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - Division
 * @a: number 1
 * @b: number 2
 * Return: Returns the calculation as an int
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - Modulo
 * @a: number 1
 * @b: number 2
 * Return: Returns the calculation as an int
 */
int op_mod(int a, int b)
{
	return (a % b);
}
